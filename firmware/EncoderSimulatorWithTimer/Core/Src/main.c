void calculateDepthValue()
{
	counter++;
	if(direction == encoderDirectionDown)
	{
		if(lastCounter > 0)
		{
			lastCounter --;
			depth = (int32_t)(((float)(lastCounter) / resolution) * 100);
			counter = 0;
		}
		else
			depth = (int32_t)(((float)(counter) / resolution) * -100);
	}
	else if(direction == encoderDirectionUp)
	{
		if(lastCounter > 0)
		{
			lastCounter --;
			depth = (int32_t)(((float)(lastCounter) / resolution) * -100);
			counter = 0;
		}
		else
			depth = (int32_t)(((float)(counter) / resolution) * 100);
	}
	else
		return;
}

void calculateSpeedValue()
{
	speed = (uint16_t)(fabsf(depth - lastDepth)) * 10.0f;
	lastDepth = depth;
}

