void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM1 || htim->Instance == TIM8)
		calculateDepthValue();

	else if(htim->Instance == TIM6)
		depthSendFlag = 1;
}

void processPacket()
{
	if(resPackFlag.resEncoderConfigFlag)
	{
		resPackFlag.resEncoderConfigFlag = 0;
		if(encoderDirectionUp == Uart.ResPackEncoderConfig.direction)
		{
			if(direction != encoderDirectionUp)
			{
			__HAL_TIM_SET_COUNTER(&htim1, htim1.Instance->ARR - (htim1.Instance->ARR / ENCODER_PHASE_DIV ));
			__HAL_TIM_SET_COUNTER(&htim8, htim8.Instance->ARR);
			direction = encoderDirectionUp;
			lastCounter  = counter;
			counter = 0;
			}
		}
		else if(encoderDirectionDown == Uart.ResPackEncoderConfig.direction)
		{
			if(direction != encoderDirectionDown)
			{
			__HAL_TIM_SET_COUNTER(&htim8, htim8.Instance->ARR - (htim8.Instance->ARR / ENCODER_PHASE_DIV));
			__HAL_TIM_SET_COUNTER(&htim1, htim1.Instance->ARR);
			direction = encoderDirectionDown;
			lastCounter  = counter;
			counter = 0;
			}
		}
		else
			return;

		htim1.Instance->PSC =  Uart.ResPackEncoderConfig.encoderSpeed;
		htim8.Instance->PSC =  Uart.ResPackEncoderConfig.encoderSpeed;
		resolution = Uart.ResPackEncoderConfig.resolution;
		sendDebugMsg((uint8_t*)"Config saved");
	}

	if(resPackFlag.resEncoderStatusFlag)
	{
		resPackFlag.resEncoderStatusFlag = 0;
		if(Uart.ResPackEncoderStatus.encoderStatus == encoderStatusStart)
		{
			if(direction == encoderDirectionUp)
			{
				HAL_TIM_Base_Stop_IT(&htim1);
				HAL_TIM_Base_Start_IT(&htim8);
				sendDebugMsg((uint8_t*)"Encoder Up started");
			}
			else if(direction == encoderDirectionDown)
			{
				HAL_TIM_Base_Start_IT(&htim1);
				HAL_TIM_Base_Stop_IT(&htim8);
				sendDebugMsg((uint8_t*)"Encoder Down started");
			}
			else
				return;

			HAL_TIM_PWM_Start_IT(&htim1, TIM_CHANNEL_1);
			HAL_TIM_PWM_Start_IT(&htim8, TIM_CHANNEL_1);
			__HAL_TIM_SET_COUNTER(&htim6, 0);
		    HAL_TIM_Base_Start_IT(&htim6);
		}
		else if(Uart.ResPackEncoderStatus.encoderStatus == encoderStatusStop )
		{
			HAL_TIM_Base_Stop_IT(&htim1);
			HAL_TIM_PWM_Stop_IT(&htim1, TIM_CHANNEL_1);
			HAL_TIM_Base_Stop_IT(&htim8);
			HAL_TIM_PWM_Stop_IT(&htim8, TIM_CHANNEL_1);
		    HAL_TIM_Base_Stop_IT(&htim6);
		    sendEncoderDepth(depth, speed);
		    sendDebugMsg((uint8_t*)"Encoder stopped");
		}
		else
			return;
	}
	if(resPackFlag.resEncoderDepthFlag)
	{
		resPackFlag.resEncoderDepthFlag = 0;
		depth = Uart.ResPackEncoderDepth.depth;
		sendEncoderDepth(depth,0);
		counter = 0;
		lastCounter = 0;
		sendDebugMsg((uint8_t*)"Encoder is reset");
	}
}
